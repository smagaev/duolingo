        <p>Типичные ошибки возникают при построении отрицаний и вопросов, потому что многие воспринимают have, как глагол, который самостоятельно их образует, а про got при этом забывают.</p>

        <p>В речи встречаются вопросы и отрицания, построенные таким образом, но мы с вами будем говорить согласно правилам, а не исключениям. Давайте запомним раз и навсегда, что если начинаем вопрос с have, то нужен got. Если в отрицании haven’t, то нужен got. Если нет got, то нужен вспомогательный глагол do или does:</p>

<div class="gray-examples"><b>Have you a car?</b> – возможно, но не нужно так говорить<br>
            <br>
    <b>Have you got a car?</b> – правильно<br>
            <br>
    <b>Do you have a car?</b> – правильно<br>
            <br>
    <b>I haven’t a brother.</b> – возможно, но не нужно так говорить<br>
            <br>
    <b>I haven’t got a brother.</b> – правильно<br>
            <br>
    <b>I do not have a brother.</b> – правильно</div>

        <p>Мы уже упоминали, что у have got нет формы прошедшего времени, поэтому отрицание и вопрос о прошлом будут следовать всем правилам времени Past Simple.</p>



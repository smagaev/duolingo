    <h2 class="text-center mb-5 ">Have и have got: различия, использование, формы</h2>
    <div class="article-text">
        <span class="h3 mobile-search-block-after-image"></span>
        <p>Вы наверняка замечали, что часто в разговорной речи наряду с глаголом <em>have</em> говорят <em>have got</em>. В связи с этим возникает много вопросов:</p>

        <p><b>Have и have got</b> – это разные глаголы или одно и тоже?<br>
            Всегда ли вместо have можно использовать have got?<br></p>

         <p>Как образовать отрицание и вопрос?<br>
            <b>Have got</b> – это что, <em>Present Perfect</em>?</p>

        <p>Эти и многие другие вопросы перестанут мучить вас после того, как вы внимательно изучите материал этой статьи.</p>

        <p>В учебниках название рассматриваемой темы обычно выглядит так: HAVE (GOT). Наличие скобок ведет студентов к заблуждению, что got – опциональный элемент, который ни на что не влияет: хочешь говори, хочешь не говори. Если хочешь блеснуть – то можно и got.</p>

        <p>На самом деле, в утвердительных предложениях такой подход может сработать, а вот формы отрицания и вопроса могут пострадать. &nbsp;Другое заблуждение – что have и have got стопроцентно заменяют друг друга по значению. Нет, это не так.</p>

        <p>В этой статье мы подробно разберем основные различия в значениях и формах глаголов have и have got. А ваша задача – запомнить и использовать их без ошибок.</p>

        <h3 class="text-center mb-4">Когда можно использовать обе формы?</h3>

        <p>Первое: обе формы используются, когда говорят о принадлежности (possession) – «у кого-то что-то есть»:</p>

        <div class="gray-examples"><b>I have/ have got a car.</b> – У меня есть машина.<br>
            <br>
            <b>He has/ has got a big house.</b> – У него есть большой дом.<br>
            <br>
            <b>I have/ have got a lot of books.</b> – У меня есть много книг.</div>

        <p>Также можно употребить оба варианта при описании внешности:</p>

        <div class="gray-examples"><b>I have/ have got brown eyes.</b> – У меня карие глаза.<br>
            <br>
            <b>She has/ has got long legs.</b> – У нее длинные ноги.<br>
            <br>
            <b>We have/ have got long hair.</b> – У нас длинные волосы.</div>

        <p>Кроме того, когда рассказывают о семье и отношениях используют и have и have got:</p>

        <div class="gray-examples"><b>I have/ have got a sister.</b> – У меня есть сестра.<br>
            <br>
            <b>He has/ has got a girlfriend.</b> – У него есть подруга.<br>
            <br>
            <b>They have/ have got a big family.</b> – У них (есть) большая семья.</div>

        <p>Обе формы можно применить, когда говорите о болезнях:</p>

        <div class="gray-examples"><b>I have / have got a headache.</b> – У меня болит голова.<br>
            <br>
            <b>She has/ has got a toothache.</b> – У нее болят зубы.</div>

        <p>Однако при выборе глагола, помните, что have got – более разговорный вариант, а have звучит более формально.</p>

        <h3 class="text-center mb-4">Когда используется только have?</h3>

        <div class="gray-examples"><b>have a good time</b> – хорошо проводить время<br>
            <br>
            <b>have breakfast</b> – завтракать<br>
            <br>
            <b>have coffee</b> – пить кофе<br>
            <br>
            <b>have a shower</b> – принимать душ</div>

        <p>Выступая в этих значениях (обозначая различные действия), глагол have может использоваться в Continuous, a have got – не может:</p>

        <div class="gray-examples"><b>I am having a good time.</b> – Я хорошо провожу время.<br>
            <br>
            <b>We are having breakfast at the moment.</b> – Мы завтракаем в данный момент.<br>
            <br>
            <b>Look, Bill is having coffee in the cafe.</b></div> – Смотрите, Билл пьет кофе в кафе.<br>
            <br>
        <b>She is having a shower now.</b> – Она сейчас принимает душ.</div>

        <p>О дополнительных значениях глагола вы можете узнать в этой статье.</p>

        <h3 class="text-center mb-4">Различия в формах</h3>

        <p>Спутать конструкции можно только в настоящем времени, потому что have got не имеет формы прошедшего времени. В прошедшем времени используется глагол had, без got:</p>

    <div class="gray-examples"><b>He has/ has got a girlfriend.</b> – У него есть подруга. (настоящее)<br>
            <br>
        <b>He had a girlfriend.</b> – У него была подруга. (прошедшее)</div>

        <p>Have got имеет сокращение и часто сокращается в утверждении и в отрицании, а глагол have не сокращается:</p>

        <div class="gray-examples"><b>I’ve got a house.</b> – У меня есть дом.<br>
            <br>
            <b>I haven’t got a flat.</b> – У меня нет квартиры.<br>
            <br><b></b>
            <b>He’s got a dog.</b></div> – У него есть собака.<br>
            <br>
    <b>He hasn’t got a cat.</b> – У него нет кошки.</div>

        <p>Если сократить форму has, когда она без got, то может получиться такое предложение как: <em>&nbsp;He’s a dog</em>. Сокращение 's можно спутать с сокращением от глагола <em>is</em> и перевести предложение: Он собака. Это может озадачить вашего собеседника, поэтому глагол have, когда он без got, лучше не сокращать. Подробнее о сокращениях в этом материале.</p>

        <p>Что касается разных типов высказывания, то в утверждении существенной разницы в формах нет.</p>

        <p><strong>Have</strong> – действует как обычный глагол во времени Present Simple, поэтому для образования отрицания и вопроса нам понадобятся вспомогательные глаголы do и does.</p>

        <p><strong>Have got</strong> – это не то же самое, что Present Perfect, это особый, нетипичный глагол из двух частей, в которой have выступает вспомогательным глаголом, а got не меняется.</p>


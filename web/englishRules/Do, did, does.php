        Do, did, does: правила, секреты, рекомендации
    </h1>
    <div itemprop="articleBody" class="balloon-row j-highlight-english-word j-not_replace_space"><p> Глагол <span
                    class="word-wrapper">do</span> в английском языке имеет огромное значение. Если вы знаете, чем
            отличаются друг от друга <span class="word-wrapper">do</span> и <span class="word-wrapper">does</span>,
            когда употребляется <span class="word-wrapper">did</span>, почему иногда мы переводим глагол <span
                    class="word-wrapper">do</span>, а иногда — нет, то вы вполне можете общаться с иностранцами и
            грамотно выражать простые мысли. </p>

        <h2> Правила <span class="word-wrapper">do</span>/<span class="word-wrapper">does</span> в английском языке
        </h2>
        <p></p>
        <h3><span class="word-wrapper">DOES</span> или <span class="word-wrapper">DO</span>: правило грамотного выбора
        </h3>
        <p> В английской речи глаголы <span class="word-wrapper">do</span> и <span class="word-wrapper">does</span>
            встречаются буквально на каждом шагу. Давайте посмотрим, когда употребляются <span
                    class="word-wrapper">do</span>/<span class="word-wrapper">does</span> и чем они вообще отличаются
            друг от друга. И <span class="word-wrapper">do</span>, и <span class="word-wrapper">does</span> переводятся
            как «делать». Более того, <span class="word-wrapper">does</span> — это всего лишь другая форма глагола <span
                    class="word-wrapper">do</span>. </p>
        <p> Употребление <span class="word-wrapper">do</span>/<span class="word-wrapper">does</span> зависит о того, о
            ком или о чем идет речь. </p>
        <p> Если мы говорим о третьем лице единственного числа (<span class="word-wrapper">he</span>/<span
                    class="word-wrapper">she</span>/<span class="word-wrapper">it</span> — он/она/оно), то к <span
                    class="word-wrapper">do</span>, как и к любому другому глаголу, добавляется окончание -<span
                    class="word-wrapper">es</span>. Таким образом, говоря о ком-то/чем-то в третьем лице, используем
            <span class="word-wrapper">does</span>. Во всех остальных лицах употребляется <span
                    class="word-wrapper">do</span>. </p>
        <p><em> <span class="word-wrapper">Mary</span> <span class="word-wrapper">does</span> <span
                        class="word-wrapper">this</span> <span class="word-wrapper">work</span> <span
                        class="word-wrapper">with</span> <span class="word-wrapper">no</span> <span
                        class="word-wrapper">enthusiasm</span>. <br> Мэри делает эту работу без малейшего энтузиазма.
            </em></p>
        <p> Не всегда подлежащее может быть выражено местоимением или одним существительным. И тогда становится сложно
            понять, что выбрать, <span class="word-wrapper">do</span> или <span class="word-wrapper">does</span>: </p>
        <p><em> <span class="word-wrapper">Mike</span> <span class="word-wrapper">and</span> <span class="word-wrapper">Jim</span>
                <span class="word-wrapper">usually</span> <span class="word-wrapper">do</span> <span
                        class="word-wrapper">their</span> <span class="word-wrapper">homework</span> <span
                        class="word-wrapper">in</span> the <span class="word-wrapper">afternoon</span>. <br> Майк и Джим
                обычно делают домашнее задание днем. </em></p>
        <p> На месте подлежащего — Майк и Джим. Их двое. Если мы заменим имена местоимением, то выберем <span
                    class="word-wrapper">they</span> — они. Третье лицо, множественное число. В данном случае
            используется <span class="word-wrapper">do</span>. </p>

        <p><span class="word-wrapper">Do</span> может выступать в качестве смыслового или вспомогательного глагола. </p>

        <p></p>
        <p> Что это значит? Большинство глаголов английского языка смысловые — они обладают смыслом, имеют перевод,
            обозначают какое-то действие (или состояние). Собственно, это самое обычное сказуемое, выраженное
            глаголом. </p>
        <p> Например: <br> <em> <span class="word-wrapper">Nancy</span> <span class="word-wrapper">bakes</span> a <span
                        class="word-wrapper">cake</span> <span class="word-wrapper">every</span> <span
                        class="word-wrapper">Sunday</span>. <br> Каждое воскресенье Нэнси печет торт. </em></p>
        <p> Не переводя глагола <span class="word-wrapper">bake</span> — печь — мы не сможем передать смысл всего
            предложения. </p>
        <p> А бывают глаголы вспомогательные. Они служат только для образования той или иной грамматической формы и не
            имеют собственного перевода в предложении. Это важная запчасть предложения, без которой оно “развалится”, но
            при этом на общий перевод этот отдельно взятый смысловой глагол влиять практически не будет. </p>
        <p> Например, вспомогательный глагол be
                (быть) в предложении: <br> <em> The <span class="word-wrapper">cake</span> <span
                        class="word-wrapper">is</span> <span class="word-wrapper">baked</span> <span
                        class="word-wrapper">by</span> <span class="word-wrapper">Nancy</span>. <br> Торт испечен Нэнси.
            </em></p>
        <p><span class="word-wrapper">Be</span> (<span class="word-wrapper">is</span>) здесь нужен только для
            образования формы пассивного залога: <span class="word-wrapper">is</span> <span
                    class="word-wrapper">baked</span>. В переводе будет звучать «испечен», но не «быть испеченным». </p>
        <p> Так и глагол <span class="word-wrapper">do</span> может быть как смысловым, так и вспомогательным. Будучи
            смысловым, он имеет перевод «делать». </p>
        <p> В качестве вспомогательного он помогает строить предложение в соответствии с правилами грамматики и не имеет
            собственного перевода. </p>
        <p><span class="word-wrapper">Do</span> как вспомогательный глагол нужен, чтобы задать вопрос или сформулировать
            отрицание в <span class="word-wrapper">Present</span> <span class="word-wrapper">Simple</span>. Если речь
            идет о <span class="word-wrapper">he</span>/<span class="word-wrapper">she</span>/<span
                    class="word-wrapper">it</span> <span class="word-wrapper">(3</span> лицо единственного числа),
            используется <span class="word-wrapper">does</span>. Как это происходит, мы сейчас посмотрим. </p>
        <h3> Вопрос с <span class="word-wrapper">do</span>/<span class="word-wrapper">does</span></h3>
        <p> При образовании вопросительного
                предложения мы ставим <span class="word-wrapper">do</span> или <span
                    class="word-wrapper">does</span> на первое место в предложении: </p>
        <p><em> <span class="word-wrapper">Do</span> <span class="word-wrapper">you</span> <span class="word-wrapper">understand</span>
                <span class="word-wrapper">me</span>? <br> Ты понимаешь меня? </em></p>
        <p><em> <span class="word-wrapper">Does</span> <span class="word-wrapper">John</span> <span
                        class="word-wrapper">understand</span> <span class="word-wrapper">anything</span>? <br> Джон
                что-нибудь понимает? </em></p>
        <p> Ни <span class="word-wrapper">do</span>, ни <span class="word-wrapper">does</span> в данном случае не
            переводятся. Их задача — поработать кирпичиками при строительстве предложения. Собственного перевода они не
            имеют. </p>
        <p> Обратите внимание, если в отрицательном или вопросительном предложении речь идет о третьем лице
            единственного числа (<span class="word-wrapper">he</span>/<span class="word-wrapper">she</span>/<span
                    class="word-wrapper">it),</span> мы используем <span class="word-wrapper">does</span> (<span
                    class="word-wrapper">do</span> + <span class="word-wrapper">es</span>) и к смысловому глаголу
            никакого окончания добавлять уже не будем — одного показателя <span class="word-wrapper">3-го</span> лица
            вполне достаточно. </p>
        <p><em> <span class="word-wrapper">Does</span> <span class="word-wrapper">John</span> <span
                        class="word-wrapper">understand</span> <span class="word-wrapper">anything</span>? (Джон
                что-нибудь понимает?) <br> <span class="word-wrapper">Does</span> <span
                        class="word-wrapper">Margaret</span> <span class="word-wrapper">live</span> <span
                        class="word-wrapper">in</span> the <span class="word-wrapper">city</span>? (Маргарет живет в
                городе?) </em></p>
        <h3> Специальный вопрос с <span class="word-wrapper">do</span>/<span class="word-wrapper">does</span></h3>
        <p> В вопросах, которые начинаются со слов <span class="word-wrapper">what</span>, <span class="word-wrapper">where</span>,
            <span class="word-wrapper">when</span>, <span class="word-wrapper">why</span> и т. п. (такой вопрос называют
            специальным), в <span class="word-wrapper">Present</span> <span class="word-wrapper">Simple</span> также
            используется вспомогательный глагол <span class="word-wrapper">do</span>/<span
                    class="word-wrapper">does</span>. И ставится он сразу после вопросительного слова: </p>
        <p><em> <span class="word-wrapper">How</span> <span class="word-wrapper">often</span> <span
                        class="word-wrapper">do</span> <span class="word-wrapper">you</span> <span class="word-wrapper">eat</span>
                <span class="word-wrapper">ice-cream</span>? <br> Как часто ты ешь мороженое? </em></p>
        <p><em> <span class="word-wrapper">Where</span> <span class="word-wrapper">does</span> <span
                        class="word-wrapper">Jack</span> <span class="word-wrapper">go</span> <span
                        class="word-wrapper">every</span> <span class="word-wrapper">summer</span>? <br> Куда Джек ездит
                каждое лето? </em></p>
        <p> Исключение: Если мы задаем вопрос к подлежащему и используем вопросительные слова <span
                    class="word-wrapper">who</span>? (кто) и <span class="word-wrapper">what</span>? (что),
            вспомогательный глагол <span class="word-wrapper">do</span>/<span class="word-wrapper">does</span> нам не
            нужен, а смысловой глагол всегда будет в третьем лице единственного числа, т.е. с окончанием <span
                    class="word-wrapper">-(e)s:</span></p>
        <p><em> <span class="word-wrapper">Who</span> <span class="word-wrapper">eats</span> <span class="word-wrapper">fish</span>?
                <br> Кто питается рыбой? </em></p>
        <p><em> <span class="word-wrapper">What</span> <span class="word-wrapper">usually</span> <span
                        class="word-wrapper">falls</span> <span class="word-wrapper">from</span> the <span
                        class="word-wrapper">sky</span> <span class="word-wrapper">in</span> <span class="word-wrapper">winter</span>?
                <br> Что обычно падает зимой с неба? </em></p>
        <p> Обратите внимание, в таком вопросе с <span class="word-wrapper">who</span> и <span
                    class="word-wrapper">what</span> может встречаться глагол <span class="word-wrapper">does</span>,
            НО… только в том случае, если это смысловой, а не вспомогательный глагол. </p>
        <p><em> <span class="word-wrapper">Who</span> <span class="word-wrapper">does</span> <span class="word-wrapper">housework</span>
                <span class="word-wrapper">at</span> <span class="word-wrapper">your</span> <span class="word-wrapper">place</span>?
                <br> Кто у вас занимается домашним хозяйством? </em></p>
        <p> В данном случае <span class="word-wrapper">does</span> ничем не отличается от любого другого глагола
            действия — <span class="word-wrapper">eat</span>, <span class="word-wrapper">dance</span>, <span
                    class="word-wrapper">play</span> и т. д. </p>
        <h3> Отрицание с <span class="word-wrapper">do</span>/<span class="word-wrapper">does</span></h3>
        <p> При образовании отрицания
            мы добавляем к <span class="word-wrapper">do</span>/<span class="word-wrapper">does</span> отрицательную
            частицу <span class="word-wrapper">not</span>: </p>
        <p><em> <span class="word-wrapper">I</span> <span class="word-wrapper">do</span> <span
                        class="word-wrapper">not</span> <span class="word-wrapper">want</span> <span
                        class="word-wrapper">to</span> <span class="word-wrapper">listen</span> <span
                        class="word-wrapper">to</span> <span class="word-wrapper">Jack</span>. <br> Не хочу слушать
                Джека. </em></p>
        <p><em> <span class="word-wrapper">He</span> <span class="word-wrapper">does</span> <span class="word-wrapper">not</span>
                <span class="word-wrapper">want</span> <span class="word-wrapper">to</span> <span class="word-wrapper">confirm</span>
                <span class="word-wrapper">our</span> <span class="word-wrapper">meeting</span>. <br> Он не хочет
                подтверждать нашу встречу. </em></p>
        <p> Как мы видим, в отрицании <span class="word-wrapper">do</span> так же, как и в вопросе, никак не
            переводится. </p>
        <p><span class="word-wrapper">Do</span> + <span class="word-wrapper">not</span> или <span class="word-wrapper">does</span>
            + <span class="word-wrapper">not</span> ставится между подлежащим и сказуемым: </p>
        <p><em> <span class="word-wrapper">Julia</span> <span class="word-wrapper">and</span> <span
                        class="word-wrapper">I</span> <span class="word-wrapper">do</span> <span class="word-wrapper">not</span>
                <span class="word-wrapper">trust</span> <span class="word-wrapper">him</span>. <br> Мы с Джулией ему не
                доверяем. </em></p>
        <p> Можно использовать сокращенную форму: <br> <span class="word-wrapper">Do</span> <span class="word-wrapper">not</span>
            = <span class="word-wrapper">don’t</span> <br> <span class="word-wrapper">Does</span> <span
                    class="word-wrapper">not</span> = <span class="word-wrapper">doesn’t</span></p>
        <p><em> <span class="word-wrapper">I</span> <span class="word-wrapper">don’t</span> <span class="word-wrapper">understand</span>
                <span class="word-wrapper">you</span>. (Я не понимаю тебя.) <br> <span class="word-wrapper">He</span>
                <span class="word-wrapper">doesn’t</span> <span class="word-wrapper">speak</span> <span
                        class="word-wrapper">Spanish</span>. (Он не говорит по-испански.) </em></p>
        <p> Помним, что когда употребляется <span class="word-wrapper">does</span> в отрицании, глагол действия
            используется без окончания <span class="word-wrapper">-(e)s:</span></p>
        <p><em> <span class="word-wrapper">Jack</span> <span class="word-wrapper">doesn’t</span> <span
                        class="word-wrapper">like</span> <span class="word-wrapper">cucumbers</span>. <br> Джек не любит
                огурцы. </em></p>
        <h3> Глагол <span class="word-wrapper">did</span>. Когда ставится, что означает, и зачем он нужен в предложении
        </h3>
        <p><span class="word-wrapper">Do</span> — неправильный глагол. Как у любого неправильного глагола, у него есть
            три формы, которые нужно знать наизусть: </p>
        <p><span class="word-wrapper">Do</span> — <span class="word-wrapper">did</span> — <span class="word-wrapper">done</span>
            <br><span class="link-like" onclick="show_sign_popup(&quot;/&quot;, false, 2);">Узнать больше</span><br></p>
        <p> Вторая форма глагола, <span class="word-wrapper">did</span>, используется в Past Simple (простое
            прошедшее время). </p>
        <p><span class="word-wrapper">Did</span> в <span class="word-wrapper">Past</span> <span class="word-wrapper">Simple</span>
            может функционировать и как смысловой, и как вспомогательный глагол. </p>
        <p> Взгляните: </p>
        <p><em> <span class="word-wrapper">He</span> <span class="word-wrapper">did</span> <span class="word-wrapper">this</span>
                <span class="word-wrapper">task</span> <span class="word-wrapper">yesterday</span>. <br> Он сделал это
                задание вчера. </em></p>
        <p><span class="word-wrapper">Did</span> в данном случае — смысловой глагол <span class="word-wrapper">do</span>
            в <span class="word-wrapper">Past</span> <span class="word-wrapper">Simple</span>, переводится он как
            «сделал». Или «делал». </p>
        <p> В вопросах и отрицаниях <span class="word-wrapper">did</span> является глаголом вспомогательным и никак не
            переводится: </p>
        <p><em> <span class="word-wrapper">Did</span> <span class="word-wrapper">you</span> <span class="word-wrapper">go</span>
                <span class="word-wrapper">to</span> the <span class="word-wrapper">South</span> <span
                        class="word-wrapper">last</span> <span class="word-wrapper">summer</span>? <br> Ты ездил на юг
                прошлым летом? </em></p>
        <p> Здесь <span class="word-wrapper">did</span> — кирпичик, который нужен, чтобы предложение было правильным с
            точки зрения грамматики. Обратите внимание: <span class="word-wrapper">did</span> стоит в <span
                    class="word-wrapper">Past</span> <span class="word-wrapper">Simple</span>. Значит, смысловой глагол
            ставить в форму прошедшего времени уже не нужно. </p>
        <p><em> <span class="word-wrapper">Did</span> <span class="word-wrapper">your</span> <span class="word-wrapper">friend</span>
                <span class="word-wrapper">call</span> <span class="word-wrapper">you</span>? <br> Твой друг звонил
                тебе? </em></p>
        <h3> Можно ли в одном предложении дважды использовать <span class="word-wrapper">do</span>? </h3>
        <p> В одном предложении глагол <span class="word-wrapper">do</span> может встречаться два раза — и в качестве
            смыслового глагола в значении «делать», и в качестве вспомогательного (в вопросах и отрицаниях группы времен
            <span class="word-wrapper">Simple).</span></p>
        <p><em> <span class="word-wrapper">Jane</span> <span class="word-wrapper">says</span>, <span
                        class="word-wrapper">she</span> <span class="word-wrapper">doesn’t</span> <span
                        class="word-wrapper">do</span> <span class="word-wrapper">this</span> <span
                        class="word-wrapper">job</span>. </em> (<span class="word-wrapper">Present</span> <span
                    class="word-wrapper">Simple</span>) <br> <em> Джейн говорит, что она этим не занимается </em>
            (дословно: не делает эту работу). </p>
        <p><span class="word-wrapper">Do</span> <span class="word-wrapper">you</span> <span
                    class="word-wrapper">always</span> <span class="word-wrapper">do</span> <span class="word-wrapper">your</span>
            <span class="word-wrapper">duties</span>? (<span class="word-wrapper">Present</span> <span
                    class="word-wrapper">Simple</span>) <br> <em> Ты всегда выполняешь свои обязанности? </em></p>
        <p><em> <span class="word-wrapper">You</span> <span class="word-wrapper">didn’t</span> <span
                        class="word-wrapper">do</span> <span class="word-wrapper">anything</span> <span
                        class="word-wrapper">to</span> <span class="word-wrapper">change</span> the <span
                        class="word-wrapper">situation</span>! </em> (<span class="word-wrapper">Past</span> <span
                    class="word-wrapper">Simple</span>) <br> Ты ничего не сделал, чтобы изменить ситуацию! </p>
        <p> Присутствие в предложении двух глаголов <span class="word-wrapper">do</span> иногда смущает студентов, но на
            самом деле ничего странного здесь нет. Один глагол несет значение, смысл, а второй нужен для правильного
            оформления предложения с точки зрения грамматики. </p>
        <p> К предложениям такого типа относится и часто употребляемый вопрос: </p>
        <p><em> <span class="word-wrapper">What</span> <span class="word-wrapper">do</span> <span class="word-wrapper">you</span>
                <span class="word-wrapper">do</span>? <br> Чем ты занимаешься? / Кем ты работаешь? </em></p>
        <h3> Почему с одним и тем же словом иногда может употребляться <span class="word-wrapper">do</span>, а иногда —
            <span class="word-wrapper">does</span>? </h3>
        <p> Интересна ситуация с коллективными (собирательными) существительными. Коллективные существительные
            номинально имеют форму единственного числа, но подразумевают группу людей. Например: <span
                    class="word-wrapper">team</span> (команда), <span class="word-wrapper">audience</span> (аудитория),
            <span class="word-wrapper">family</span> (семья) и т.д. </p>
        <p> Такие существительные могут согласовываться с глаголами и в единственном, и во множественном числе: </p>
        <p><em> <span class="word-wrapper">My</span> <span class="word-wrapper">family</span> <span
                        class="word-wrapper">is</span> <span class="word-wrapper">big</span>. <br> Моя семья большая.
            </em></p>
        <p> Здесь мы рассматриваем семью как единое целое и <span class="word-wrapper">family</span> в данном случае
            требует после себя глагола в единственном числе. </p>
        <p><em> <span class="word-wrapper">My</span> <span class="word-wrapper">family</span> <span
                        class="word-wrapper">live</span> <span class="word-wrapper">in</span> <span
                        class="word-wrapper">different</span> <span class="word-wrapper">countries</span>. <br> Моя
                семья живет по разным странам. </em></p>
        <p> Здесь для более корректного перевода мы могли бы «семью» заменить на «членов моей семьи». И так становится
            тем более очевидно, что речь идет о нескольких людях, но не о семье в целом. </p>
        <p> Если вам встречается подобное коллективное существительное, уделите особое внимание переводу: в каком
            значении употреблено это слово, речь о едином целом или об отдельных людях? </p>
        <p> Возвращаясь к <span class="word-wrapper">do</span>/<span class="word-wrapper">does</span>: именно от того,
            насколько правильно вы проанализируете значение коллективного существительного, будет зависеть выбор <span
                    class="word-wrapper">do</span> или <span class="word-wrapper">does</span>: </p>
        <p><em> The <span class="word-wrapper">team</span> <span class="word-wrapper">do</span> <span
                        class="word-wrapper">their</span> <span class="word-wrapper">best</span>! <br> Команда делает
                все возможное! </em> (каждый из игроков выкладывается на <span class="word-wrapper">100</span> %) </p>
        <p><em> The <span class="word-wrapper">team</span> <span class="word-wrapper">doesn’t</span> <span
                        class="word-wrapper">do</span> <span class="word-wrapper">without</span> a <span
                        class="word-wrapper">goalkeeper</span>! <br> Команде не обойтись без вратаря! </em> (команда как
            единое целое нуждается во вратаре, а не отдельные игроки) </p>
        <p> Обратите внимание. В американском английском понятия коллективных существительных нет. Мы ставим <span
                    class="word-wrapper">does</span>, когда используется существительное в третьем лице и единственном
            числе — и неважно, что у него есть коллективное значение. Номинально это единственное число, значит,
            выбираем <span class="word-wrapper">does</span>: </p>
        <p><em> The <span class="word-wrapper">firm</span> <span class="word-wrapper">does</span> <span
                        class="word-wrapper">very</span> <span class="word-wrapper">well</span>. </em> (<span
                    class="word-wrapper">Am</span>. <span class="word-wrapper">Eng.)</span> <br> <em> Фирма
                преуспевает. </em></p>
        <h3><span class="word-wrapper">Do</span> в значении «не так ли» </h3>
        <p> Глаголы <span class="word-wrapper">do</span>/<span class="word-wrapper">did</span>/<span
                    class="word-wrapper">does</span>, когда употребляются в вопросах с «хвостиком»,
            переводятся как «не так ли». </p>
        <p> Взгляните: </p>
        <p><em> <span class="word-wrapper">Mary</span> <span class="word-wrapper">eats</span> <span
                        class="word-wrapper">porridge</span> <span class="word-wrapper">every</span> <span
                        class="word-wrapper">day</span>, <span class="word-wrapper">doesn’t</span> <span
                        class="word-wrapper">she</span>? (Мэри ест кашу каждый день, не так ли?). </em></p>
        <p><em> <span class="word-wrapper">Mary</span> <span class="word-wrapper">doesn’t</span> <span
                        class="word-wrapper">eat</span> <span class="word-wrapper">porridge</span> <span
                        class="word-wrapper">every</span> <span class="word-wrapper">day</span>, <span
                        class="word-wrapper">does</span> <span class="word-wrapper">she</span>? (Мэри не ест кашу каждый
                день, не так ли?). </em></p>
        <p> Основное предложение мы составляем по правилам образования <span class="word-wrapper">Present</span> <span
                    class="word-wrapper">Simple</span>. Если предложение утвердительное, то «хвостик» отрицательный, как
            здесь: </p>
        <p><em> <span class="word-wrapper">Mary</span> <span class="word-wrapper">eats</span> <span
                        class="word-wrapper">porridge</span> <span class="word-wrapper">every</span> <span
                        class="word-wrapper">day</span>, <span class="word-wrapper">doesn’t</span> <span
                        class="word-wrapper">she</span>? (Мэри ест кашу каждый день, не так ли?). </em> <br> В данном
            случае вспомогательный глагол появляется только в «хвостике». </p>
        <p> Если основное предложение отрицательное, то «хвостик» положительный, а вспомогательный глагол появляется в
            обеих частях предложения: </p>
        <p><em> <span class="word-wrapper">Mary</span> <span class="word-wrapper">doesn’t</span> <span
                        class="word-wrapper">eat</span> <span class="word-wrapper">porridge</span> <span
                        class="word-wrapper">every</span> <span class="word-wrapper">day</span>, <span
                        class="word-wrapper">does</span> <span class="word-wrapper">she</span>? (Мэри не ест кашу каждый
                день, не так ли?) </em></p>
        <p> В прошедшем времени (<span class="word-wrapper">Past</span> <span class="word-wrapper">Simple</span>) мы
            будем использовать в «хвостике» вспомогательный глагол <span class="word-wrapper">did</span>: </p>
        <p><em> <span class="word-wrapper">You</span> <span class="word-wrapper">never</span> <span
                        class="word-wrapper">wanted</span> <span class="word-wrapper">to</span> <span
                        class="word-wrapper">see</span> <span class="word-wrapper">me</span> <span class="word-wrapper">at</span>
                <span class="word-wrapper">your</span> <span class="word-wrapper">place</span>, <span
                        class="word-wrapper">did</span> <span class="word-wrapper">you</span>? <br> Ты же никогда не
                хотел видеть меня в своем доме, разве не так? </em></p>
        <p> В данном случае мы образовали «хвостик» с помощью положительной формы <span class="word-wrapper">did</span>,
            так как основное предложение имеет отрицательный смысл благодаря слову-маркеру <span class="word-wrapper">never</span>
            — никогда. </p>
        <h3><span class="word-wrapper">Do</span> как заменитель глагола действия </h3>
        <p> В некоторых случаях <span class="word-wrapper">do</span> появляется, казалось бы, совсем непонятно почему.
            Посмотрим на такой мини-диалог: </p>
        <p><em> — <span class="word-wrapper">I</span> <span class="word-wrapper">eat</span> <span class="word-wrapper">porridge</span>
                <span class="word-wrapper">every</span> <span class="word-wrapper">day</span>. (Я ем кашу каждый день)
                <br> — <span class="word-wrapper">Do</span> <span class="word-wrapper">you</span>? (Правда?) </em></p>
        <p> Наш собеседник использовал время <span class="word-wrapper">Present</span> <span
                    class="word-wrapper">Simple</span>: <span class="word-wrapper">I</span> <span class="word-wrapper">eat</span>
            <span class="word-wrapper">porridge</span> <span class="word-wrapper">every</span> <span
                    class="word-wrapper">day</span>. </p>
        <p> Мы задаем уточняющий вопрос, но при этом мы не станем дублировать смысловой глагол <span
                    class="word-wrapper">eat</span> — это будет звучать некрасиво и не очень грамотно. Вместо этого мы
            используем вспомогательный глагол <span class="word-wrapper">do</span>: <span class="word-wrapper">Do</span>
            <span class="word-wrapper">you</span>? </p>
        <p> Перевести в данном значении <span class="word-wrapper">do</span> мы можем по-разному: «Правда?», «Неужели?»,
            «Серьезно?» «В самом деле?» — в зависимости от интонации и контекста. Но никак не «Ты делаешь?» или
            что-нибудь подобное. </p>
        <h3> Когда <span class="word-wrapper">do</span>, <span class="word-wrapper">did</span>, <span
                    class="word-wrapper">does</span> употребляются для усиления значения </h3>
        <p><span class="word-wrapper">Do</span> (<span class="word-wrapper">did</span>/<span
                    class="word-wrapper">does</span>) в качестве вспомогательного глагола может появляться не только в

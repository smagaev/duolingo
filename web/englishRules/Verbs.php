<h2>Let’s talk about Phrasal Verbs!</h2>
<p>&nbsp;</p>

<h4>Some verbs  are made of two words.</h4>

<ul>
<li>You can <strong>get out</strong>of the car.
</li><li>We can <stong>get around</stong> on our bikes.</li>
</ul>

<h4>Some two‑word verbs don’t need to stay together.</h4>

<ul><li>
        I need to pick up my son.</li><li>
        I need to pick my son up.</li>
</ul>
<h4>
    Words like it, him, etc. must go between the two parts of the verb!</h4>
<ul>
    <li>I need to pick him up.</li>
    <li>I need to drop her off.</li>
</ul>

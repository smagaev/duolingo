<strong>Few / Little</strong>  - означают "мало", "недостаточное количество чего-то" и имеют отрицательный оттенок.
<p></p><strong>Few</strong>  - используется с исчисляемыми существительными во множественном числе.</p>
<p><strong>Little</strong> - используется с неисчисляемыми существительными.</p>
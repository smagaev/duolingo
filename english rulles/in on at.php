in  - когда говорим о чем то в целом, масштабном:
      1. века, года, месяц, времена года, промежуток времени, morning, evening, afternoon, future
      2. страны, города, районы
      3. my room
on -  более специфическое, более конкретное, меньшее по размеру
      1. день недели, число, день рождения, the first of june, friday morning
      2. улица, транспорт, facebook и все что в интернете
      Примечание: обычно если есть day - то предлог on

at -  очень конкретное место или время
      2. 7am, midnight, night, Christmas
      Примечание: нет Day



